package com.company;

public class Whileloop {
    public static void main(String[] args){
        /*
        Syntax of while loop:
        while(condition){
            //body
        }
         */

        //Simple program printing numbers from 0 to 9 using while loop

        int num = 1;
        while(num<10){
            System.out.println(num);
            num++;
        }
    }
}
