package com.company;

public class dowhileloop {
    public static void main(String[] args){
        /*
        Syntax of do while loop
        do{
            //body
        }while(condition);
         */
        int num = 1;
        do {
            System.out.println(num);
            num++;
        }while(num<10);

        // Will execute at least once
    }
}
